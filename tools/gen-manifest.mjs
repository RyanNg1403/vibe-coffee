// Regenerates src/soundManifest.js and CREDITS.md from whatever is in
// public/sounds/ plus the attribution data in tools/sound-credits.json.
// Usage: node tools/gen-manifest.mjs
import { readdirSync, readFileSync, writeFileSync, existsSync } from 'fs';
import { parseFile } from 'music-metadata';

const REPO = new URL('..', import.meta.url).pathname;
const SOUNDS_DIR = REPO + 'public/sounds';
const CREDITS_JSON = REPO + 'tools/sound-credits.json';

const LOOPS = new Set(['chatter', 'chatter2', 'traffic_day', 'traffic_night', 'rain_window']);

const credits = existsSync(CREDITS_JSON) ? JSON.parse(readFileSync(CREDITS_JSON, 'utf8')) : {};

const entries = [];
for (const f of readdirSync(SOUNDS_DIR).sort()) {
  if (!/\.(mp3|ogg)$/.test(f)) continue;
  const key = f.replace(/\.(mp3|ogg)$/, '');
  const meta = await parseFile(`${SOUNDS_DIR}/${f}`);
  entries.push({
    key,
    url: `/sounds/${f}`,
    loop: LOOPS.has(key),
    durationSec: Math.round((meta.format.duration ?? 0) * 10) / 10,
  });
}

const manifest = `// Recorded sound assets. THIS FILE IS REGENERATED by tools/gen-manifest.mjs
// after the asset-gathering step — do not hand-edit entries.
// Missing/undownloadable entries fall back to synthesis automatically.
// Levels are auto-normalized at load time (see soundLoader.js).

export const SOUND_MANIFEST = {
${entries.map((e) => `  ${e.key}: { url: '${e.url}', loop: ${e.loop} }, // ${e.durationSec}s`).join('\n')}
};

// attribution data lives in CREDITS.md
`;
writeFileSync(REPO + 'src/soundManifest.js', manifest);

// 3D models
const MODELS_DIR = REPO + 'public/models';
const modelEntries = [];
if (existsSync(MODELS_DIR)) {
  for (const f of readdirSync(MODELS_DIR).sort()) {
    if (!f.endsWith('.glb')) continue;
    modelEntries.push({ key: f.replace(/\.glb$/, ''), url: `/models/${f}` });
  }
}
const modelManifest = `// 3D model assets. THIS FILE IS REGENERATED by tools/gen-manifest.mjs —
// do not hand-edit entries. Missing entries fall back to procedural props.

export const MODEL_MANIFEST = {
${modelEntries.map((e) => `  ${e.key}: { url: '${e.url}' },`).join('\n')}
};

// attribution data lives in CREDITS.md
`;
writeFileSync(REPO + 'src/modelManifest.js', modelManifest);
entries.push(...modelEntries.map((e) => ({ ...e, model: true })));

// surface textures: literal URLs so the artifact bundler can inline them
const TEX_DIR = REPO + 'public/textures';
const texEntries = [];
if (existsSync(TEX_DIR)) {
  for (const f of readdirSync(TEX_DIR).sort()) {
    if (!/\.(jpg|png|webp)$/.test(f)) continue;
    texEntries.push({ key: f, url: `/textures/${f}` });
  }
}
const texManifest = `// Surface texture assets. THIS FILE IS REGENERATED by tools/gen-manifest.mjs —
// do not hand-edit entries.

export const TEXTURE_MANIFEST = {
${texEntries.map((e) => `  '${e.key}': '${e.url}',`).join('\n')}
};
`;
writeFileSync(REPO + 'src/textureManifest.js', texManifest);
entries.push(...texEntries.map((e) => ({ ...e, texture: true })));

const lines = entries.map((e) => {
  const c = credits[e.key];
  if (!c) return `- **${e.key}** — source metadata missing (see git history of tools/sound-credits.json)`;
  return `- **${e.key}** — "${c.title ?? e.key}"${c.creator ? ` by ${c.creator}` : ''} — ${c.license ?? 'license unrecorded'} — [source](${c.item_url ?? c.source_url ?? '#'})`;
});
const creditsMd = `# Sound credits

The recorded sound assets in \`public/sounds/\` come from the sources below
(Creative Commons / public domain). Trimming was the only modification.
Everything not listed here (all music, plus any sound whose recording failed
to load) is synthesized at runtime with WebAudio.

${lines.join('\n')}
`;
writeFileSync(REPO + 'CREDITS.md', creditsMd);

console.log(`manifest: ${entries.length} assets\n` + entries.map((e) => `  ${e.key} (${e.durationSec}s)`).join('\n'));
